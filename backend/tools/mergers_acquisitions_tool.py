    if any(term in question for term in ["방어", "적대적", "독약", "황금낙하산", "백기사", "백지주", "황금주", "차등의결권", "팩맨", "왕관", "불가침", "녹색편지", "행동주의"]):
        return explain_takeover_defenses()

